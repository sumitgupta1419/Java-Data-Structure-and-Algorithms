public class AssignmentOperator {
    public static void main(String[] args) {
        int p =10;
        int q;

        // =
        q=p;
        System.out.println(q);

        // +=
        p+=q;  //p=p+q
        System.out.println(p);

        p-=q;
        System.out.println(p);

        p*=q;
        System.out.println(p);
    }
}
